// Helper function to get song by number
export const getSongByNumber = (songNumber: number): Song | undefined => {
  return thirupugazhSongs.find(song => song.songNumber === songNumber);
};

// Helper function to get songs by place
export const getSongsByPlace = (place: string, language: 'en' | 'ta'): Song[] => {
  const placeKey = language === 'en' ? 'english' : 'tamil';
  return thirupugazhSongs.filter(song => 
    song.place[placeKey].toLowerCase().includes(place.toLowerCase())
  );
};
